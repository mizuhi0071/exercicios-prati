function repetirNumero(numero) {       // Cria uma função que recebe um número como parâmetro
    for (let repetirNumero = 0; repetirNumero < 10; repetirNumero++) {      // Cria um loop que se repete 10 vezes
        console.log(numero);        // Exibe o número no console
    }
}
