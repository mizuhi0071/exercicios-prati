function fibonacci(n) {
    let fib = [0, 1]; // Inicializa a sequência com os dois primeiros números
    for (let i = 2; i < n; i++) {
        fib[i] = fib[i - 1] + fib[i - 2]; // Calcula o próximo número da sequência
    }
    return fib;
}
